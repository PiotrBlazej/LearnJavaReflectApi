package methods;

public abstract class AbstractCar {
	public String model;
	private long milage;

	public long getMilage() {
		return milage;
	}

	public void setMilage(long milage) {
		this.milage = milage;
	}


	public String getModel() {
		return model;
	}


	public void setModel(String model) {
		this.model = model;
	}


	
	
	
	

}
